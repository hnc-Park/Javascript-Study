// 산술 연산자 (Arithmetic Operators)
// +
// -
// * 곱하기
// / 나누기 (실수 몫)
// % 나머지
// ** 거듭제곱

console.log(5 + 2);
console.log(5 - 2);
console.log(5 * 2);
console.log(5 / 2);
console.log(5 ** 2);

// 문자열의 덧셈
let text = '두개의' + '문자를';
console.log(text);

text = '1' + 1; // 숫자와 문자열을 더하면 문자열 // '11'
console.log(text);
